-- Consolidated schema.sql for fresh Supabase Database initialization

-- 1. Enable pgvector extension
CREATE EXTENSION IF NOT EXISTS vector;

-- 2. Users Table
CREATE TABLE IF NOT EXISTS public.users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username TEXT UNIQUE NOT NULL,
  PASSWORD TEXT NOT NULL, -- Hashed password
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  
  -- Profile Fields
  age INT,
  weight NUMERIC,
  height NUMERIC,
  sport_history TEXT,
  running_experience TEXT,
  past_injuries TEXT,
  lifestyle TEXT,
  weekly_availability TEXT,
  terrain_preference TEXT,
  equipment TEXT,
  
  -- JSONB fields for flexibility
  goals JSONB DEFAULT '{}'::jsonb,
  
  -- Integrations
  garmin_email TEXT,
  garmin_password TEXT, -- Encrypted
  strava_access_token TEXT,
  strava_refresh_token TEXT,
  strava_last_updated TIMESTAMP WITH TIME ZONE,
  
  -- Coach State
  coach_status TEXT DEFAULT 'initial',
  interview_chat_id BIGINT,
  training_plan JSONB,
  training_plan_generated_at TIMESTAMP WITH TIME ZONE,
  training_plan_phased JSONB,
  training_plan_phased_at TIMESTAMP WITH TIME ZONE,
  
  -- Onboarding/Interview state
  interview_started_at TIMESTAMP WITH TIME ZONE,
  interview_completed_at TIMESTAMP WITH TIME ZONE,
  interview_step INT DEFAULT 1,
  
  -- Garmin Sync State
  garmin_sync_status VARCHAR(50) DEFAULT 'not_connected',
  garmin_sync_progress INTEGER DEFAULT 0,
  garmin_last_sync_error TEXT,
  garmin_sync_started_at TIMESTAMP WITH TIME ZONE,
  garmin_sync_completed_at TIMESTAMP WITH TIME ZONE
);


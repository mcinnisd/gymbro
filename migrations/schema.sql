-- 9. User Baselines Table
CREATE TABLE IF NOT EXISTS public.user_baselines (
    id SERIAL PRIMARY KEY,
    user_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
    metric_category VARCHAR(50) NOT NULL,  -- 'running', 'health', 'training_load'
    baselines JSONB NOT NULL,              -- Computed baseline values
    computed_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(user_id, metric_category)
);
CREATE INDEX IF NOT EXISTS idx_user_baselines_user_id ON user_baselines(user_id);

-- 10. Training Events Table (Calendar)
CREATE TABLE IF NOT EXISTS public.training_events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT REFERENCES public.users(id) ON DELETE CASCADE NOT NULL,
    date DATE NOT NULL,
    title TEXT NOT NULL,
    description TEXT,
    event_type TEXT CHECK (event_type IN ('run', 'strength', 'rest', 'race', 'other')),
    status TEXT CHECK (status IN ('planned', 'completed', 'skipped')) DEFAULT 'planned',
    created_by TEXT CHECK (created_by IN ('user', 'coach')) DEFAULT 'user',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);
CREATE INDEX IF NOT EXISTS idx_training_events_user_date ON training_events(user_id, date);

-- 11. User Intelligence Table (Unified memory & RAG facts)
CREATE TABLE IF NOT EXISTS public.user_intelligence (
    id SERIAL PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    category VARCHAR(50) NOT NULL, 
    -- 'fact', 'preference', 'goal', 'injury', 'nutrition', 'training_philosophy', 'interaction'
    content TEXT NOT NULL,
    metadata JSONB DEFAULT '{}',
    embedding vector(768), -- Gemini standard dimension
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_intelligence_user_id ON user_intelligence(user_id);
CREATE INDEX IF NOT EXISTS idx_intelligence_category ON user_intelligence(category);
CREATE INDEX IF NOT EXISTS idx_intelligence_embedding_hnsw ON user_intelligence USING hnsw (embedding vector_cosine_ops);

-- 12. Search function for the new intelligence layer
CREATE OR REPLACE FUNCTION match_intelligence (
    query_embedding vector(768),
    match_user_id int,
    match_count int DEFAULT 5,
    match_threshold float DEFAULT 0.5,
    filter_categories varchar[] DEFAULT NULL
)
RETURNS TABLE (
    id int,
    content text,
    category varchar,
    similarity float
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT
        ui.id,
        ui.content,
        ui.category,
        1 - (ui.embedding <=> query_embedding) AS similarity
    FROM user_intelligence ui
    WHERE ui.user_id = match_user_id
    AND (filter_categories IS NULL OR ui.category = ANY(filter_categories))
    AND 1 - (ui.embedding <=> query_embedding) > match_threshold
    ORDER BY ui.embedding <=> query_embedding
    LIMIT match_count;
END;
$$;

-- 13. Trigger for updated_at
CREATE OR REPLACE FUNCTION update_intelligence_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER trigger_intelligence_updated_at
    BEFORE UPDATE ON user_intelligence
    FOR EACH ROW
    EXECUTE FUNCTION update_intelligence_updated_at();
